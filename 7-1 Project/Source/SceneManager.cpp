 /***********************************************************
  *  RenderBook()
  *
  *  This method is called to render the shapes for the book
  *  object using multiple boxes to create a more 3D appearance.
  ***********************************************************/
void SceneManager::RenderBook()
{
	// declare the variables for the transformations
	glm::vec3 scaleXYZ;
	float XrotationDegrees = 0.0f;
	float YrotationDegrees = 0.0f;
	float ZrotationDegrees = 0.0f;
	glm::vec3 positionXYZ;

	/*** Main book body ***/
	// set the XYZ scale for the mesh (book proportions - laying flat)
	scaleXYZ = glm::vec3(2.2f, 0.08f, 1.5f);  // width, thickness, depth

	// set the XYZ rotation for the mesh
	XrotationDegrees = 0.0f;
	YrotationDegrees = -12.0f;  // slight rotation like Ready Player One book
	ZrotationDegrees = 0.0f;

	// set the XYZ position for the mesh (place it on the table)
	positionXYZ = glm::vec3(-1.2f, 0.59f, 0.3f);  // sitting on table

	// set the transformations into memory to be used on the drawn meshes
	SetTransformations(
		scaleXYZ,
		XrotationDegrees,
		YrotationDegrees,
		ZrotationDegrees,
		positionXYZ);

	// Set orange color similar to Ready Player One book
	SetShaderColor(1.0f, 0.5f, 0.1f, 1.0f);  // Orange color
	SetShaderMaterial("wood");  // Use existing wood material

	// draw the main book body
	m_basicMeshes->DrawBoxMesh();

	/*** Book spine (slightly thicker edge) ***/
	// set the XYZ scale for the spine
	scaleXYZ = glm::vec3(0.08f, 0.08f, 1.5f);  // thin width, slightly thicker, same depth as book

	// rotate spine to face directly toward camera (square with camera)
	XrotationDegrees = 0.0f;
	YrotationDegrees = -12.0f;  // rotated 90 degrees to face directly forward
	ZrotationDegrees = 0.0f;

	// position the spine at the side edge facing the viewer
	positionXYZ = glm::vec3(-2.3f, 0.59f, 0.3f);

	SetTransformations(
		scaleXYZ,
		XrotationDegrees,
		YrotationDegrees,
		ZrotationDegrees,
		positionXYZ);

	// Slightly darker orange for the spine
	SetShaderColor(0.9f, 0.4f, 0.05f, 1.0f);  // Darker orange
	SetShaderMaterial("wood");

	// draw the book spine
	m_basicMeshes->DrawBoxMesh();

	/*** Book cover details (small raised rectangle for title area) ***/
	// set the XYZ scale for the cover detail
	scaleXYZ = glm::vec3(2.15f, 0.06f, 1.45f);  // smaller than main book, very thin

	// keep same rotation
	XrotationDegrees = 0.0f;
	YrotationDegrees = -12.0f;
	ZrotationDegrees = 0.0f;

	// position slightly above the main book surface
	positionXYZ = glm::vec3(-1.2f, 0.57f, 0.3f);

	SetTransformations(
		scaleXYZ,
		XrotationDegrees,
		YrotationDegrees,
		ZrotationDegrees,
		positionXYZ);

	// Even brighter orange for the cover detail
	SetShaderColor(0.95f, 0.95f, 0.9f, 1.0f);  // Bright orange
	SetShaderMaterial("wood");

	// draw the cover detail
	m_basicMeshes->DrawBoxMesh();
}

